public enum AnimationType
{
    Idle,
    Walk,
    Run,
    Jump,
    Straight,       // 전방을 겨냥하고 진동
    Right,          // 오른쪽으로 휘두름
    Left,           // 왼쪽으로 휘두름
    Up,             // 수직으로 들음
    Down,           // 아래로 내려침
    Shot,           // 총처럼 쏨
    Stick,          // 땅에 수직으로 꽂음
    Block,          // 가로로 막음
    Hit             // 피격
}
